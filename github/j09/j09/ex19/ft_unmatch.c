nt	ft_unmatch(int *tab, int length)
{
	int i;
	int j;
	int r;

	r = 0;
	i = 0;
	while (i <= length)
	{
		j = 0;
		while (j < length)
		{
			if (tab[i] == tab[j])
				r++;
			j++;
		}
		if (r % 2 != 0)
			return (tab[i]);
		i++;
	}
	return (0);
}
